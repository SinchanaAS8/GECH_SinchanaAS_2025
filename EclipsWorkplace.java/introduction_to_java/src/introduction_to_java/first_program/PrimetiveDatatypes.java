package introduction_to_java.first_program;

public class PrimetiveDatatypes {

	public static void main(String[] args) {
		// Primetive Data Types
		/*
		 * 1. byte = 23 (default = 0)
		 * 2. short =   (default = 0)
		 * 3. int (default = 0)
		 * 4. long (default = 0l)
		 * 5. float (default = 0.0f)
		 * 6. double (default = 0.0)
		 * 7. char (default = '')
		 * 8. boolean (default = false)
		 */
		
		//decleration of variables
		/*
		 * variable - It is 
		 */
		
		byte age;
		age = 22;
		
		byte age1 = 19;
		
		System.out.println(age);
		System.out.println(age1);
		
		short a = 2020;
		int b = 32000;
		long c = 300L;
		float d = 34.5f;
		double e = 10.5;
		char alp = 'S';
		boolean isdigit = true;
		System.out.println("value of a: "+a);
		System.out.println("value of b: "+b);
		System.out.println("value of c: "+c);
		System.out.println("value of d: "+d);
		System.out.println("value of e: "+e);
		System.out.println("value of alp: "+alp);
		System.out.println("boolean: "+isdigit);
 
	}

}
